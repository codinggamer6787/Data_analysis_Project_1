# Commented out IPython magic to ensure Python compatibility.
import pandas as pd
pd.plotting.register_matplotlib_converters()
import matplotlib.pyplot as plt
# %matplotlib inline
import seaborn as sns
print("Setup Complete")

cancer_filepath ="/content/world_smoking_history_1924_2023 (1).csv"

cancer_data= pd.read_csv(cancer_filepath)

sns.lineplot(data=cancer_data);

plt.figure(figsize=(10, 6))
sns.histplot(cancer_data['Male_Smokers_Percentage'], kde=True, color='blue', label='Male Smokers', bins=30, alpha=0.5)
sns.histplot(cancer_data['Female_Smokers_Percentage'], kde=True, color='red', label='Female Smokers', bins=30, alpha=0.5)
plt.title('Overlapping Histograms of Male and Female Smokers')
plt.xlabel('Number of Smokers in Percentage')
plt.ylabel('Frequency')
plt.legend()
plt.show()

g = sns.FacetGrid(cancer_data, col="Country", col_wrap=4, height=4)
g.map(sns.histplot, "Male_Smokers_Percentage", kde=True, bins=30)
g.add_legend()
plt.show()

import matplotlib.pyplot as plt
import seaborn as sns


# Set the width and height of the figure
plt.figure(figsize=(14, 6))

# Add title
plt.title("Comparison of Smokers Percentage and Average Cigarettes Per Day")


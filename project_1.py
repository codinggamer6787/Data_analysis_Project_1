sns.lineplot(data=cancer_data, x='Year', y='Male_Smokers_Percentage', label="Male Smokers (%)")
sns.lineplot(data=cancer_data, x='Year', y='Female_Smokers_Percentage', label="Female Smokers (%)")

# Create a second y-axis for the average cigarettes per day
ax1 = plt.gca()
ax2 = ax1.twinx()

sns.lineplot(data=cancer_data, x='Year', y='Average_Cigarettes_Per_Day', label="Avg Cigarettes Per Day", ax=ax2, color="green")

# Add labels for the axes
ax1.set_xlabel("Year")
ax1.set_ylabel("Smokers Percentage")
ax2.set_ylabel("Average Cigarettes Per Day")

# Add legends
ax1.legend(loc='upper left')
ax2.legend(loc='upper right')

# Display the plot
